/// Quran verse (ayah) data model
class QuranVerse {
  final int id;
  final int sura;
  final int aya;
  final String text;

  const QuranVerse({
    required this.id,
    required this.sura,
    required this.aya,
    required this.text,
  });

  factory QuranVerse.fromMap(Map<String, dynamic> map) {
    return QuranVerse(
      id: map['id'] as int,
      sura: map['sura'] as int,
      aya: map['aya'] as int,
      text: map['text'] as String? ?? '',
    );
  }

  Map<String, dynamic> toMap() {
    return {
      'id': id,
      'sura': sura,
      'aya': aya,
      'text': text,
    };
  }

  /// Get a short preview of the verse text
  String get preview {
    if (text.length <= 120) return text;
    return '${text.substring(0, 120)}...';
  }

  /// Unique cache key: "sura_aya" — e.g. "2_255" for Ayatul Kursi
  String get cacheKey => 'quran_${sura}_$aya';

  @override
  String toString() => 'QuranVerse($sura:$aya)';
}

